{
    console.log("=== While Loop ===");

    let i = 1;
    while (i <= 5) {
        console.log("Number:", i);
        i++;
    }
}

{
    console.log("=== Do...While Loop ===");

    let j = 1;
    do {
        console.log("Number:", j);
        j++;
    } while (j <= 5);
}

{
    console.log("=== For Loop ===");

    for (let k = 1; k <= 5; k++) {
        console.log("Number:", k);
    }
}
